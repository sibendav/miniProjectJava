    @Test
    public void oursphere() {
        Scene scene = new Scene("Test scene");
        scene.set_camera(new Camera(new Point3D(0, 0, -1000), new Vector(0, 0, 1), new Vector(0, -1, 0)));
        scene.set_distance(500);
        scene.set_background(Color.BLACK);
        scene.set_ambientLight(new AmbientLight(Color.BLACK, 0));

        scene.addGeometries(
                new Sphere(new Color(java.awt.Color.GREEN), new Material(0.5, 0.5, 100), 50, new Point3D(0, 30, 50)));

        scene.addLights(new DirectionalLight(new Color(0, 0, 500), new Vector(1, -1, 1)), new SpotLight(new Color(1500, 0, 0),
                new Point3D(60, 80, -50),new Vector(1, -1, 2), 1, 0.00001, 0.00000001),new PointLight(new Color(2000, 60, 200),
                new Point3D(10, 20, 130),
                1, 0.0005, 0.0005));

        ImageWriter imageWriter = new ImageWriter("our_sphere", 150, 150, 500, 500);
        render render = new render(imageWriter, scene);

        render.renderImage();
        render.writeToImage();

    }
